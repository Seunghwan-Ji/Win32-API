#include "CCore.h"

CCore* CCore::g_pInst = nullptr; // 헤더 파일의 정적 멤버 변수 초기화.

// private로 선언된 정적 멤버 변수는 클래스 외부에서 직접 접근할 수 없지만,
// 초기화는 해당 클래스의 소속임을 명시하는 방식으로 가능하다.
// 이 방식은 C++의 표준에서 허용되며, 클래스 외부에서 정적 멤버를 초기화하려면
// 반드시 클래스의 범위를 명시해야 한다.

// 1. 클래스 소속 변수임을 명시 :
// CCore::g_pInst처럼** 범위 연산자(::)** 를 사용하여 클래스와의 소속 관계를 표시합니다.

// 2. 정적 변수는 클래스의 객체와 독립적 :
// 정적 변수는 클래스의 인스턴스와 독립적으로 관리되며, 프로그램의 데이터 영역에 위치합니다.
// 따라서 생성자나 인스턴스가 없어도 초기화할 수 있습니다.

// 3. 접근 지정자의 제약을 따름 :
// private로 선언된 변수는 클래스 내부의 멤버 함수에서만 접근할 수 있습니다.
// 하지만 초기화는 컴파일러가 데이터 영역에 메모리를 할당하는 작업으로,
// 클래스 소속임을 명시하면 외부에서도 허용됩니다.